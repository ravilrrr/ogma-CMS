class Url{

	public static $uri  = array('matches'=>array());
	public static $route_match = '';

	public function __construct() {
    	// nothing
    }

    /**
     * Add a trainilg slash to a path if it doesn't exist
     */
    public static function tsl($path) {
		if( substr($path, strlen($path) - 1) != '/' ) {
			$path .= '/';
		}
		return $path;
	}

	/**
	 * Returns a full URL 
	 */
	public static function returnUrl($uri){
		return self::tsl(Core::$site['siteurl']).$uri;
	}

	/**
	 * Returns a Page ID 
	 * Routing tables are checked first the page slugs
	 * 
	 * Credit to Toro.php for inspiration and the regexs. 
	 */
	public static function getPageID(){
		$uri = trim(str_replace('index.php', '', $_SERVER['REQUEST_URI']), '/#');
		
		$parts=explode('/',$uri);
		$handler_name = '';
		$allpages = Core::$pages;
		//check how many parts in the URL
		// if more than 1 check against routes. 
		if (count($parts)>=1 && $parts[0]!=''){
			$request_method = strtolower($_SERVER['REQUEST_METHOD']);
	        $path_info = '/';
	        if (!empty($_SERVER['PATH_INFO'])) {
	            $path_info = $_SERVER['PATH_INFO'];
	        }
	        else if (!empty($_SERVER['ORIG_PATH_INFO']) && $_SERVER['ORIG_PATH_INFO'] !== '/index.php') {
	            $path_info = $_SERVER['ORIG_PATH_INFO'];
	        }
	        else {
	            if (!empty($_SERVER['REQUEST_URI'])) {
	                $path_info = (strpos($_SERVER['REQUEST_URI'], '?') > 0) ? strstr($_SERVER['REQUEST_URI'], '?', true) : $_SERVER['REQUEST_URI'];
	            }
	        }
	        
	        $regex_matches = array();
			$discovered_handler = '';
			$handler_name  = '';
	        $route = Core::$routes;
	        $routes = $route->getCache()->get();

	        $tokens = array(
	                ':string' => '([a-zA-Z]+)',
	                ':number' => '([0-9]+)',
	                ':alpha'  => '([a-zA-Z0-9-_]+)'
	        );

	        if ($routes) {
	            
	            foreach ($routes as $router) {
	            	
	            	$pattern=strtr($router['route'] , $tokens);

	   	                if (preg_match('#^/?' . $pattern . '/?$#', $path_info, $matches)) {
	            
	                	$handler_name  = $router['page'];
	                    $discovered_handler = $handler_name;
	                    Url::$uri['matches'] = $matches;
	                    Url::$route_match = $router['slug'];
	                    
	                    break;
	                }
	            }
	        }
	      if (Url::$uri['matches']!=''){

    			$pages= $allpages->getCache()->unique('slug')->get();
		    	$pageid = in_array($parts[0], $pages) ? $parts[0] : '404'; 
		    	$handler_name = $pageid;
		    } else {
		    	$handler_name = "index";
		    }
	    } 
    	
    	if ($handler_name==''){;
	    	$allpages = Core::$pages;
	      	if (count(Url::$uri['matches'])>0){
     			$pages= $allpages->getCache()->unique('slug')->get();
 		    	$pageid = in_array($parts[0], $pages) ? $parts[0] : '404'; 
 		    	$handler_name = $pageid;
 		    } else {
 		    	$handler_name = "index";
 		    }
	    }

	    Url::$uri['id'] = $handler_name;
		return $handler_name;

	}

	/**
	 * Return URI matches if a routing match exists
	 */
	public static function getMatches(){
		return Url::$uri['matches'];
	}

	public static function getRouteMatch(){
		return Url::$route_match;
	}

  

